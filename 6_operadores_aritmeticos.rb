numero1 = 12
numero2 = 3

# OPERADOR DE SUMA +
# Resultado de suma
puts 'El resultado de la suma es: '
puts numero1 + numero2 # 15

# OPERADOR DE RESTA -
puts 'El resultado de la resta es: '
puts numero1 - numero2 # 9

# OPERADOR DE MULTIPLICACION *

puts 'El resultado de la multiplicacion es: '
puts numero1 * numero2 # 36


# OPERADOR DE DIVISION /

puts 'El resultado de la division es: '
puts numero1 / numero2 # 4
