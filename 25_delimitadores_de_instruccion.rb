# TEMA: Delimitadores de instrucción

=begin
  Los delimitadores de instrucción en Ruby
  son caracteres o símbolos que se utilizan
  para separar y terminar instrucciones o
  expresiones en el código. Estos delimitadores
  son esenciales para indicar al intérprete de
  Ruby dónde comienza y termina cada instrucción,
  lo que ayuda a que el código sea legible y comprensible.
=end

# DELIMITADOR *;*
num1 = 10; num2 = 20; num3 = 30

puts num1

# DELIMITADOR *\*
#  el \ se usan con comillas dobles para secuencias de escape
nombre = "Lorem \
ipsum"

puts nombre
