# // OPERADOR MODULO
# En Ruby, el operador % se utiliza para realizar una operación de módulo, que devuelve el residuo de la división de un número por otro. Por ejemplo:
# ruby
# ---- puts 30 % 7 ----
# En este caso, 30 % 7 calculará el residuo de dividir 30 entre 7. El resultado de esta operación será 2, ya que 7 cabe en 30 cuatro veces (7 * 4 = 28) y sobran 2. Por lo tanto, el resultado impreso será 2.

puts 30 % 7 # 2



